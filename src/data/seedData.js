function normalizeUnitValue(value) {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/\./g, "")
    .replace(/\s+/g, " ");
}

function getSeedUnit(unitId, unitValue) {
  const resolvedUnitId = unitId || unitAliases[normalizeUnitValue(unitValue)];

  if (!resolvedUnitId) {
    return null;
  }

  return initialUnits.find((unit) => unit.id === resolvedUnitId) || null;
}

function withManagedUnit(row) {
  const matchedUnit = getSeedUnit(row.unitId, row.unit);

  if (!matchedUnit) {
    return row;
  }

  return {
    ...row,
    unitId: matchedUnit.id,
    unit: matchedUnit.abbreviation,
  };
}

